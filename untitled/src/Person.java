public class Person {
    // properties
    private String name;
    private int age;
    private float height;
    private boolean gender;

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setHeight(float height) {
        this.height = height;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public String getName(){
        return name;
    }


}
